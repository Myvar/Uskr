void keyboard_handler(struct regs *r)
{
    unsigned char scancode;

    /* Read from the keyboard's data buffer */
    scancode = inportb(0x60);

   
    if (scancode & 0x80)
    {
 
    }
    else
    {       
        //putc(kbdus[scancode]);
        KeyboardIrq(kbdus[scancode]);
    }
}


char convert_scancode(int c)
{
    return kbdus[c];
}

